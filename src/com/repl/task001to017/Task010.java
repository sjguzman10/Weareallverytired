package com.repl.task001to017;

public class Task010 {

	public static void main(String[] args) {
		/*
		 * 1. Declare 2 numbers and assign values 200 and 100 respectively.
		 * 2. First, add numbers, then subtract, third multiply, fourth divide.
		 * 3. Please use variables to print the values of each operation result on a separate line. 
		 * OUTPUT = 300, 100, 20000, 2
		 */

		int num1=200;
		int num2=100;
		
		int add=num1+num2;
		int sub=num1-num2;
		int mult=num1*num2;
		int div=num1/num2;
		
		System.out.println(add);
		System.out.println(sub);
		System.out.println(mult);
		System.out.println(div);
				
	}

}
